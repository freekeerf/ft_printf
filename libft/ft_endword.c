#include "libft.h"

int		ft_endword(const char *str, size_t n)
{
	if (str && n)
	{
		while (!((str[n] >= 9 && str[n] <= 13) || str[n] == 32))
			n++;
		return (n - 1);
	}
	return (-1);
}
